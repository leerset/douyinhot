set :branch, 'master'
set :forward_agent, true
set :app_path, lambda { "#{deploy_to}/#{current_path}" }
set :stage, 'production'
set :rvm_path, '/home/deploy/.rvm/bin/rvm'

set :shared_paths, ['config/database.yml', 'log']

